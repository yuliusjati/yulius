package operatoraritmatika2;

public class operatorUnary {
    
    public static void main(String[] args) {
        int x = 1;
        int y = 0;
        
        y = ++x;
        System.out.println("Nilai y sekarang : " + y);
        System.out.println("Nilai x sekarang : " + x);
       
    }
    
}
